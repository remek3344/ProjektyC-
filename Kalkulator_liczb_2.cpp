#include <iostream>
using namespace std;
int main()
{
    float liczba1, liczba2;
    cout << "podaj dwie liczby\n";
    cin >> liczba1;
    cin >> liczba2;
    cout << liczba1 << " + " << liczba2 << " = " << liczba1 + liczba2 << endl;
    cout << liczba1 << " - " << liczba2 << " = " << liczba1 - liczba2 << endl;
    cout << liczba1 << " * " << liczba2 << " = " << liczba1 * liczba2 << endl;
    cout << liczba1 << " / " << liczba2 << " = " << liczba1 / liczba2 << endl;
    return 0;
}